# 1.按经验将不同变量存储为不同类型的数据
# 2.验证数据类型--检测数据类型--type()
a = 10
b = 10.28
c = True
print(type(a))
print(type(b))
print(type(c))
# list--列表
d = [10, 20, 30]
print(type(d))
# tuple--元组
e = (10, 20, 30)
print(type(e))
# set--集合
f = {10, 20, 30}
print(type(f))
# dict--字典--键值对
g = {'name': 'tom', 'age': 18}
print(type(g))
